public class AverageMarks{
	public static void main(String[]args){
		double ITP, ITC, LA, Eng1, Sum, Average;
		ITP = 97.2;
		ITC = 78;
		LA = 85.2;
		Eng1 = 68;
		Sum = ITP + ITC + LA + Eng1;
		Average = Sum/4;
		System.out.println("Here are my marks in my top 4 subjects");
		System.out.println("ITP = " + ITP + "\nITC = " + ITC + "\nLA = " + LA + "\nEng1 = " + Eng1);
		System.out.println("Sum of these Subjects is " + Sum);
		System.out.println("Average Marks are " + Average);
	}
}
 
